    private void FollowUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FollowUserActionPerformed
        if (!IDs.contains(UserIDArea.getText())) {//check if user exists
            JOptionPane.showMessageDialog(null, "User does not exist.", "Follow Error", JOptionPane.INFORMATION_MESSAGE);
            UserIDArea.setText("");
            return;
        } else if (user.getFollowers().contains(UserIDArea.getText())) {//check if already following
            JOptionPane.showMessageDialog(null, "You are already following this user.", "Follow Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            following.addElement(UserIDArea.getText()); //add to JList
            CurrentFollowing.setModel(following);
            int index = 0;
            for(User use : users) //get index of person to follow
                if(use.getID().equals(UserIDArea))
                    index = users.indexOf(use);
            user.attach(users.get(index)); //attach to their following list
            
        }
        
        UserIDArea.setText("");
    }//GEN-LAST:event_FollowUserActionPerformed

    private void TweetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TweetActionPerformed
         if (TweetMessage.getText().equals("")) {//empty input error
            JOptionPane.showMessageDialog(null, "Nothing Tweeted", "No Tweet", JOptionPane.INFORMATION_MESSAGE);
        } else{
             user.message(TweetMessage.getText());
             newsFeed.addElement("Me: " + TweetMessage.getText()); //add to own newsfeed
             NewsFeed.setModel(newsFeed);
             TweetMessage.setText("");
         }
         List<User> notify = user.getFollowers();//notify followers of tweet
         String message = user.getID() + " - " + TweetMessage.getText();
         for(User observer: notify){
           observer.addNews(message);
         }
    }//GEN-LAST:event_TweetActionPerformed


